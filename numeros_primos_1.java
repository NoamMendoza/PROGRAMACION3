    private void txt_numerosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_numerosKeyTyped
        if(txt_numeros.getText().length()>=4){
            evt.consume();
        }else{
            int key = evt.getKeyChar();
            boolean numeros = key >= 48 && key <=57;
            if(!numeros){
                evt.consume();
            }
        }
    }//GEN-LAST:event_txt_numerosKeyTyped

