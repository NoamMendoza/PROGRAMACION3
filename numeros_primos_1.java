    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txt_numeros.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un numero entero positivo por favor.", "Error", JOptionPane.ERROR_MESSAGE);
            txt_numeros.requestFocus();
        }else{
            int valor = 0;
            String menssage = "";
            valor = Integer.parseInt(txt_numeros.getText());
            calcula_si_num_es_primo obj = new calcula_si_num_es_primo();
            menssage = ""+ obj.Primo(valor);
            JOptionPane.showMessageDialog(null, "El numero "+ valor + " "+ menssage+".", "Informacion.", JOptionPane.INFORMATION_MESSAGE);
            txt_numeros.setText("");
            txt_numeros.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

